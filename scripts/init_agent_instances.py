async def main():
    """Initialize default agent instances."""
    print("正在初始化默认智能体实例...")

    manager = AgentInstanceManager()
    await manager.load()

    # Check if there are already instances
    existing_instances = await manager.list_instances()
    if existing_instances:
        print(f"已存在 {len(existing_instances)} 个智能体实例")
        print("是否要清除现有实例并重新初始化？(y/N): ", end="")
        response = input().strip().lower()
        if response != 'y':
            print("跳过初始化。")
            return
        # Clear existing instances
        await manager.clear_all()
        print("已清除现有实例。")

    # Create default instances
    created_count = 0
    for agent_data in DEFAULT_AGENTS:
        try:
            instance = await manager.create_instance(**agent_data)
            print(f"✓ 创建智能体：{agent_data['name']} ({instance.id})")
            created_count += 1
        except Exception as e:
            print(f"✗ 创建智能体失败：{agent_data['name']} - {e}")

    print(f"\n初始化完成！共创建 {created_count} 个智能体实例。")
    print("\n你可以在前端控制台访问 http://127.0.0.1:8088/agent-instances 查看和管理智能体实例。")


if __name__ == "__main__":
    asyncio.run(main())
